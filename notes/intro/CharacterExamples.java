package java_project_2;

public class CharacterExamples {
	public static void main(String[] args) {
		// Define the char variables
		char exampleCharacter = 'A';
		char exampleUnicodeChar = '\u0041';
		String mathSetSymbol = "\ud835\udd6b";
		
		// Print out the characters defined above
		System.out.println("Character A: " + exampleCharacter);
		System.out.println("Unicode \\u0041: " + exampleUnicodeChar);
		System.out.println("Unicode \\ud835\\udd6b: " + mathSetSymbol);
	}
}
